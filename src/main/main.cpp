// bar_poll_keyboard (defined below) is the single input source — BAR_AUTOPLAY script or live
// keyboard. It feeds BOTH the high-level path (this callback, for ultramodern's osContGetReadData)
// and the low-level SI path (os_unimpl_stubs.cpp's __osSiRawStartDma fill), so input reaches the
// game whichever way it polls.
extern "C" uint16_t bar_poll_keyboard(int port, int8_t* stick_x, int8_t* stick_y);

static bool input_get(int controller_num, uint16_t* buttons, float* x, float* y) {
    if (controller_num != 0) {
        return false;   // only port 1 is "connected"
    }
    int8_t sx = 0, sy = 0;
    const uint16_t b = bar_poll_keyboard(0, &sx, &sy);
    if (buttons != nullptr) *buttons = b;
    if (x != nullptr)       *x = (float)sx / 80.0f;   // N64 stick range -> ~[-1, 1]
    if (y != nullptr)       *y = (float)sy / 80.0f;
    return true;
}

