// Keyboard -> N64 controller (port 0). BAR reads the pad via the raw SI/PIF path, so
// __osSiRawStartDma_recomp (os_unimpl_stubs.cpp) calls this to fill the read buffer. Uses Win32 async
// key state, gated to when the game window is focused. Bindings (port 0):
//   Analog stick: Arrows   A: X   B: Z   Start: Enter   Z-trigger: Shift   L: Q   R: W   C-buttons: I/J/K/L   D-pad: T/F/G/H
// Scripted auto-input for headless menu/race testing. Set BAR_AUTOPLAY to a space-separated
// sequence of "frames:hexbuttons" phases, played in order then held neutral. Hex is the N64 button
// mask (START=1000 A=8000 B=4000 Z=2000; d-pad U=0800 D=0400 L=0200 R=0100). D-pad bits also drive
// the analog stick so menus reading either respond. Bypasses the focus gate.
// Example: BAR_AUTOPLAY="180:0 12:1000 60:0 12:8000"  -> wait, tap START, wait, tap A.
static uint16_t bar_autoplay_poll(const char* script, int8_t* stick_x, int8_t* stick_y) {
    struct Phase { int dur; uint16_t btn; };
    static std::vector<Phase> phases;
    static bool parsed = false;
    static size_t idx = 0;
    static int held = 0;
    static unsigned long long fc = 0;
    if (!parsed) {
        parsed = true;
        for (const char* p = script; *p; ) {
            while (*p == ' ' || *p == '\t') ++p;
            if (!*p) break;
            char* e = nullptr;
            long dur = std::strtol(p, &e, 10);
            uint16_t btn = 0;
            if (e && *e == ':') btn = (uint16_t)std::strtol(e + 1, &e, 16);
            p = (e && e != p) ? e : p + 1;
            if (dur > 0) phases.push_back({(int)dur, btn});
        }
        std::fprintf(stderr, "[BeetleRecomp] AUTOPLAY: %zu phases parsed\n", phases.size());
        std::fflush(stderr);
    }
    ++fc;
    uint16_t btn = 0;
    if (idx < phases.size()) {
        btn = phases[idx].btn;
        if (++held >= phases[idx].dur) {
            held = 0; ++idx;
            std::fprintf(stderr, "[BeetleRecomp] AUTOPLAY -> phase %zu/%zu @fc=%llu next=0x%04X\n",
                         idx, phases.size(), fc, (unsigned)(idx < phases.size() ? phases[idx].btn : 0));
            std::fflush(stderr);
        }
    }
    int sx = 0, sy = 0;
    if (btn & 0x0800) sy += 80;
    if (btn & 0x0400) sy -= 80;
    if (btn & 0x0200) sx -= 80;
    if (btn & 0x0100) sx += 80;
    if (stick_x) *stick_x = (int8_t)sx;
    if (stick_y) *stick_y = (int8_t)sy;
    return btn;
}

extern "C" uint16_t bar_poll_keyboard(int port, int8_t* stick_x, int8_t* stick_y) {
    if (stick_x) *stick_x = 0;
    if (stick_y) *stick_y = 0;
    if (port != 0) return 0;
    static const char* autoplay = std::getenv("BAR_AUTOPLAY");
    if (autoplay && *autoplay) return bar_autoplay_poll(autoplay, stick_x, stick_y);
    if (g_bar_hwnd != nullptr && GetForegroundWindow() != g_bar_hwnd) return 0; // only when focused
    auto down = [](int vk) { return (GetAsyncKeyState(vk) & 0x8000) != 0; };
    uint16_t b = 0;
    if (down('X'))       b |= 0x8000; // CONT_A
    if (down('Z'))       b |= 0x4000; // CONT_B
    if (down(VK_SHIFT))  b |= 0x2000; // CONT_G (Z trigger)
    if (down(VK_RETURN)) b |= 0x1000; // CONT_START
    if (down('T'))       b |= 0x0800; // D-pad up
    if (down('G'))       b |= 0x0400; // D-pad down
    if (down('F'))       b |= 0x0200; // D-pad left
    if (down('H'))       b |= 0x0100; // D-pad right
    if (down('Q'))       b |= 0x0020; // CONT_L
    if (down('W'))       b |= 0x0010; // CONT_R
    if (down('I'))       b |= 0x0008; // C-up
    if (down('K'))       b |= 0x0004; // C-down
    if (down('J'))       b |= 0x0002; // C-left
    if (down('L'))       b |= 0x0001; // C-right
    int sx = 0, sy = 0;
    if (down(VK_LEFT))  sx -= 80;
    if (down(VK_RIGHT)) sx += 80;
    if (down(VK_UP))    sy += 80;   // N64 stick: up is positive
    if (down(VK_DOWN))  sy -= 80;
    if (stick_x) *stick_x = (int8_t)sx;
    if (stick_y) *stick_y = (int8_t)sy;
    return b;
}

// Error handling (error_handling::callbacks_t).
static void message_box(const char* msg) { std::fprintf(stderr, "[BeetleRecomp] %s\n", msg); }

int main(int argc, char** argv) {
    (void)argc; (void)argv;
#ifdef _WIN32
    { extern void bar_install_crash_handler(); bar_install_crash_handler(); }   // symbolized backtrace on AV
#endif
#ifdef _WIN32
    // Raise the Windows timer resolution to 1ms. CRITICAL for menu/sparse-workload framerate: the game's
    // VI-synced RCP scheduler + our runtime's gfx/task threads hand work off via timed waits. At the default
    // ~15.6ms timer resolution, each wake quantizes to ~15.6ms, so a single gfx round-trip costs ~2 quanta
    // (~32ms) and a menu frame several — capping the menu at ~10fps. (A race never waits — it always has gfx
    // queued — so it was unaffected and this was wrongly dismissed as "jitter only".) winmm is linked.
    timeBeginPeriod(1);
#endif

    recomp::Version project_version{};
    if (!recomp::Version::from_string(version_string, project_version)) {
        message_box(("Invalid version string: " + version_string).c_str());
        return EXIT_FAILURE;
    }

    // Config/save directory. TODO(BAR): use a proper per-user app folder, not cwd.
    recomp::register_config_path(std::filesystem::current_path());

    // Teach librecomp about BAR (hash / entrypoint / save type).
    for (const auto& game : supported_games) {
        recomp::register_game(game);
    }

    // Wire the generated section/overlay tables so BAR's ~130 relocatable modules resolve at runtime.
    bar_register_overlays();
    // TODO(BAR): bar::register_patches(); once patches exist.

    // ---- Pick + validate the ROM, then start the game (no menu UI yet) ----
    // The game thread blocks in wait_for_game_started() until start_game() runs; with no UI to
    // pick a ROM, we do it here. create_render_context() runs inside preinit *before*
    // wait_for_game_started(), so starting the game now does not race renderer init.
    std::filesystem::path rom_path = (argc > 1)
        ? std::filesystem::path(argv[1])
        : std::filesystem::path("C:/Users/Bryan/Downloads/Beetle Adventure Racing (USA).z64"); // TODO(BAR): real ROM picker

    std::u8string game_id = supported_games[0].game_id;
    recomp::RomValidationError rom_err = recomp::select_rom(rom_path, game_id);
    if (rom_err != recomp::RomValidationError::Good) {
        message_box(("Could not load ROM \"" + rom_path.string() + "\" (validation error "
                     + std::to_string(static_cast<int>(rom_err)) + ")").c_str());
        return EXIT_FAILURE;
    }
    // Start the game once the renderer is up. The VI thread only seeds a dummy OSViMode while
    // !is_game_started() (ultramodern events.cpp); starting before its first tick makes update_vi()
    // dereference a null mode. recomp::start() below blocks running the event loop, so defer this to
    // a detached thread that waits for the renderer-ready signal plus a margin for the first VI tick.
    std::thread([game_id]() {
        // Wait until the renderer has presented at least one frame (g_bar_vi_ticked), which means the
        // VI thread has ticked and seeded a dummy OSViMode. Starting before that races the VI thread
        // into a null-mode deref. Cap the wait (~5s) so we never hang if presentation stalls.
        for (int i = 0; i < 500 && !g_bar_vi_ticked.load(); i++) {
            std::this_thread::sleep_for(std::chrono::milliseconds(10));
        }
        recomp::start_game(game_id);
    }).detach();

    // ---- Assemble callbacks (field names verified against the vendored headers) ----
    recomp::rsp::callbacks_t rsp_callbacks{};                  // MANDATORY
    rsp_callbacks.get_rsp_microcode = get_rsp_microcode;

    ultramodern::renderer::callbacks_t renderer_callbacks{};   // MANDATORY
    renderer_callbacks.create_render_context = bar_create_rt64_render_context;

    ultramodern::gfx_callbacks_t gfx_callbacks{};
    gfx_callbacks.create_gfx    = create_gfx;
    gfx_callbacks.create_window = create_window;
    gfx_callbacks.update_gfx    = update_gfx;

    ultramodern::audio_callbacks_t audio_callbacks{};
    audio_callbacks.queue_samples        = queue_samples;
    audio_callbacks.get_frames_remaining = get_frames_remaining;
    audio_callbacks.set_frequency        = set_frequency;

    ultramodern::input::callbacks_t input_callbacks{};
    input_callbacks.poll_input = input_poll;
    input_callbacks.get_input  = input_get;
    input_callbacks.set_rumble = input_set_rumble;
    input_callbacks.get_connected_device_info = input_get_device_info;

    ultramodern::error_handling::callbacks_t error_handling_callbacks{};
    error_handling_callbacks.message_box = message_box;

    ultramodern::threads::callbacks_t threads_callbacks{};
    threads_callbacks.get_game_thread_name = get_game_thread_name;

    recomp::Configuration config{};
    config.project_version          = project_version;
    config.rsp_callbacks            = rsp_callbacks;
    config.renderer_callbacks       = renderer_callbacks;
    config.audio_callbacks          = audio_callbacks;
    config.input_callbacks          = input_callbacks;
    config.gfx_callbacks            = gfx_callbacks;
    config.error_handling_callbacks = error_handling_callbacks;
    config.threads_callbacks        = threads_callbacks;
    // MENU-FPS FIX: BAR's SI (controller) event queue (main.c:311, D_8002F828) is only capacity 3, and our
    // low-level __osSiRawStartDma stub posts an SI completion per call. BAR doesn't drain them all during
    // the menu, so the queue fills with stale completions; with the default requeue_si=true, every new SI
    // completion then BOUNCES (re-queues) off the full queue in a ~15M/sec busy-spin that starves the
    // cooperative scheduler and throttled the menu to ~10fps. SI completions are disposable (live input is
    // re-read from PIF RAM each frame), so drop the excess instead of bouncing it.
    config.message_queue_control.requeue_si = false;
    // config.window_handle / events_callbacks: defaults are fine for now.

    // Cooperative audio-thread preemption: raise a host "yield now" flag at ~500Hz; a poll injected at every
    // recompiled-function prologue (fix-recompiled.sh rule F) then yields to the audio manager so long
    // straight-line game-thread compute can't starve audio (-> no underrun crackle at a low buffer). The flag
    // is gated to game threads once running, so starting the timer before the game spins up is harmless.
    extern void bar_start_preempt_timer();
    extern void bar_stop_preempt_timer();
    bar_start_preempt_timer();

    // Blocks until the game exits.
    recomp::start(config);
    bar_stop_preempt_timer();
    return EXIT_SUCCESS;
}
