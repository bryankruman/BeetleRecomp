// Keyboard -> N64 controller (port 0). BAR reads the pad via the raw SI/PIF path, so
// __osSiRawStartDma_recomp (os_unimpl_stubs.cpp) calls this to fill the read buffer. Uses Win32 async
// key state, gated to when the game window is focused. Bindings (port 0):
//   Analog stick: Arrows   A: X   B: Z   Start: Enter   Z-trigger: Shift   L: Q   R: W   C-buttons: I/J/K/L   D-pad: T/F/G/H
// Scripted auto-input for headless menu/race testing. Set BAR_AUTOPLAY to a space-separated
// sequence of "frames:hexbuttons" phases, played in order then held neutral. Hex is the N64 button
// mask (START=1000 A=8000 B=4000 Z=2000; d-pad U=0800 D=0400 L=0200 R=0100). D-pad bits also drive
// the analog stick so menus reading either respond. Bypasses the focus gate.
// Example: BAR_AUTOPLAY="180:0 12:1000 60:0 12:8000"  -> wait, tap START, wait, tap A.
static uint16_t bar_autoplay_poll(const char* script, int8_t* stick_x, int8_t* stick_y) {
    struct Phase { int dur; uint16_t btn; };
    static std::vector<Phase> phases;
    static bool parsed = false;
    static size_t idx = 0;
    static int held = 0;
    static unsigned long long fc = 0;
    if (!parsed) {
        parsed = true;
        for (const char* p = script; *p; ) {
            while (*p == ' ' || *p == '\t') ++p;
            if (!*p) break;
            char* e = nullptr;
            long dur = std::strtol(p, &e, 10);
            uint16_t btn = 0;
            if (e && *e == ':') btn = (uint16_t)std::strtol(e + 1, &e, 16);
            p = (e && e != p) ? e : p + 1;
            if (dur > 0) phases.push_back({(int)dur, btn});
        }
        std::fprintf(stderr, "[BeetleRecomp] AUTOPLAY: %zu phases parsed\n", phases.size());
        std::fflush(stderr);
    }
    ++fc;
    uint16_t btn = 0;
    if (idx < phases.size()) {
        btn = phases[idx].btn;
        if (++held >= phases[idx].dur) {
            held = 0; ++idx;
            std::fprintf(stderr, "[BeetleRecomp] AUTOPLAY -> phase %zu/%zu @fc=%llu next=0x%04X\n",
                         idx, phases.size(), fc, (unsigned)(idx < phases.size() ? phases[idx].btn : 0));
            std::fflush(stderr);
        }
    }
    int sx = 0, sy = 0;
    if (btn & 0x0800) sy += 80;
    if (btn & 0x0400) sy -= 80;
    if (btn & 0x0200) sx -= 80;
    if (btn & 0x0100) sx += 80;
    if (stick_x) *stick_x = (int8_t)sx;
    if (stick_y) *stick_y = (int8_t)sy;
    return btn;
}

// Fold the latest controller snapshot (sampled on the main thread by bar_sample_gamepad) into the pad
// state: OR the buttons, and use the controller's left stick only if the keyboard left it neutral.
static void bar_poll_gamepad(uint16_t* btn, int* sx, int* sy) {
    const uint32_t s = g_pad_state.load(std::memory_order_relaxed);
    *btn |= (uint16_t)(s & 0xFFFF);
    if (*sx == 0 && *sy == 0) {
        *sx = (int)(int8_t)((s >> 16) & 0xFF);
        *sy = (int)(int8_t)((s >> 24) & 0xFF);
    }
}

// BAR_SHOTS="frame:path frame:path ..." — request an internal-render screenshot (RT64 GPU readback -> PNG)
// at each listed frame, on the SAME frame counter as BAR_AUTOPLAY so captures line up with scripted input.
extern "C" void bar_rt64_request_screenshot(const char *path);
static void bar_shots_poll() {
    struct Shot { unsigned long frame; std::string path; };
    static std::vector<Shot> shots;
    static bool parsed = false;
    static size_t idx = 0;
    static unsigned long long fc = 0;
    if (!parsed) {
        parsed = true;
        if (const char *s = std::getenv("BAR_SHOTS")) {
            for (const char *p = s; *p; ) {
                while (*p == ' ' || *p == '\t') ++p;
                if (!*p) break;
                char *e = nullptr;
                long fr = std::strtol(p, &e, 10);
                if (e && *e == ':') {
                    const char *ps = e + 1, *pe = ps;
                    while (*pe && *pe != ' ' && *pe != '\t') ++pe;
                    if (fr > 0 && pe > ps) shots.push_back({ (unsigned long)fr, std::string(ps, pe - ps) });
                    p = pe;
                } else {
                    p = (e && e != p) ? e : p + 1;
                }
            }
            std::fprintf(stderr, "[BeetleRecomp] BAR_SHOTS: %zu scheduled\n", shots.size());
        }
    }
    ++fc;
    while (idx < shots.size() && fc >= shots[idx].frame) {
        bar_rt64_request_screenshot(shots[idx].path.c_str());
        std::fprintf(stderr, "[BeetleRecomp] BAR_SHOTS -> %s @fc=%llu\n", shots[idx].path.c_str(), (unsigned long long)fc);
        ++idx;
    }
}

// BAR_SHOT_BURST="fc:dir:count" — at input frame fc, tell RT64 to capture the next `count` presents to
// dir/fNNNN.png (one per present). For animations the input timeline can't sample (film-roll transition:
// the game blocks in a render loop and stops polling input, so BAR_SHOTS frames freeze mid-roll). Trigger
// a few frames BEFORE the transition, while fc is still advancing; RT64 then counts presents on its own.
extern "C" void bar_rt64_start_burst(const char *dir, int count);
static void bar_burst_poll() {
    static bool parsed = false, fired = false;
    static unsigned long fc_trigger = 0; static int count = 0; static std::string dir;
    static unsigned long long fc = 0;
    if (!parsed) {
        parsed = true;
        if (const char *s = std::getenv("BAR_SHOT_BURST")) {
            std::string spec(s);
            size_t c1 = spec.find(':'), c2 = (c1 == std::string::npos) ? std::string::npos : spec.find(':', c1 + 1);
            if (c2 != std::string::npos) {
                fc_trigger = std::strtoul(spec.substr(0, c1).c_str(), nullptr, 10);
                dir = spec.substr(c1 + 1, c2 - c1 - 1);
                count = std::atoi(spec.substr(c2 + 1).c_str());
            }
            std::fprintf(stderr, "[BeetleRecomp] BAR_SHOT_BURST: fc=%lu dir=%s count=%d\n", fc_trigger, dir.c_str(), count);
        }
    }
    ++fc;
    if (!fired && count > 0 && fc >= fc_trigger) {
        fired = true;
        bar_rt64_start_burst(dir.c_str(), count);
        std::fprintf(stderr, "[BeetleRecomp] BAR_SHOT_BURST triggered @fc=%llu\n", (unsigned long long)fc);
        std::fflush(stderr);
    }
}

extern "C" uint16_t bar_poll_keyboard(int port, int8_t* stick_x, int8_t* stick_y) {
    if (stick_x) *stick_x = 0;
    if (stick_y) *stick_y = 0;
    if (port != 0) return 0;
    bar_shots_poll();
    bar_burst_poll();
    static const char* autoplay = std::getenv("BAR_AUTOPLAY");
    if (autoplay && *autoplay) return bar_autoplay_poll(autoplay, stick_x, stick_y);
#ifdef BEETLE_ENABLE_UI
    // While the cheats/settings overlay is open, freeze game input so menu navigation (arrows/enter)
    // doesn't also drive the car.
    if (bar_ui::menu_capturing_input()) return 0;
#endif
    if (g_bar_hwnd != nullptr && GetForegroundWindow() != g_bar_hwnd) return 0; // only when focused
    auto down = [](int vk) { return (GetAsyncKeyState(vk) & 0x8000) != 0; };
    uint16_t b = 0;
    if (down('X'))       b |= 0x8000; // CONT_A
    if (down('Z'))       b |= 0x4000; // CONT_B
    if (down(VK_SHIFT))  b |= 0x2000; // CONT_G (Z trigger)
    if (down(VK_RETURN)) b |= 0x1000; // CONT_START
    if (down('T'))       b |= 0x0800; // D-pad up
    if (down('G'))       b |= 0x0400; // D-pad down
    if (down('F'))       b |= 0x0200; // D-pad left
    if (down('H'))       b |= 0x0100; // D-pad right
    if (down('Q'))       b |= 0x0020; // CONT_L
    if (down('W'))       b |= 0x0010; // CONT_R
    if (down('I'))       b |= 0x0008; // C-up
    if (down('K'))       b |= 0x0004; // C-down
    if (down('J'))       b |= 0x0002; // C-left
    if (down('L'))       b |= 0x0001; // C-right
    int sx = 0, sy = 0;
    if (down(VK_LEFT))  sx -= 80;
    if (down(VK_RIGHT)) sx += 80;
    if (down(VK_UP))    sy += 80;   // N64 stick: up is positive
    if (down(VK_DOWN))  sy -= 80;
    bar_poll_gamepad(&b, &sx, &sy);   // fold in an SDL game controller if one is connected
    if (stick_x) *stick_x = (int8_t)sx;
    if (stick_y) *stick_y = (int8_t)sy;
    return b;
}

// Error handling (error_handling::callbacks_t).
static void message_box(const char* msg) { std::fprintf(stderr, "[BeetleRecomp] %s\n", msg); }

// "Restart Game" (pause menu): the recompiled game can't be cleanly re-entered in-process, so restart
// the whole process — relaunch the exe with the same arguments, then quit this instance. Declared in
// src/ui/bar_ui.cpp. Non-static so the UI TU can link to it.
void bar_restart_game() {
#ifdef _WIN32
    char exe[MAX_PATH] = {};
    GetModuleFileNameA(nullptr, exe, MAX_PATH);
    std::string cmd = GetCommandLineA();   // relaunch with the same args (ROM path, etc.) + same cwd
    STARTUPINFOA si{}; si.cb = sizeof(si);
    PROCESS_INFORMATION pi{};
    if (CreateProcessA(exe, cmd.empty() ? nullptr : cmd.data(), nullptr, nullptr, FALSE, 0,
                       nullptr, nullptr, &si, &pi)) {
        CloseHandle(pi.hProcess);
        CloseHandle(pi.hThread);
    } else {
        std::fprintf(stderr, "[BeetleRecomp] restart: CreateProcess failed (%lu)\n", (unsigned long)GetLastError());
    }
#endif
    ultramodern::quit();   // exit this instance (the fresh one takes over)
}

int main(int argc, char** argv) {
    (void)argc; (void)argv;
#ifdef _WIN32
    { extern void bar_install_crash_handler(); bar_install_crash_handler(); }   // symbolized backtrace on AV
#endif
#ifdef _WIN32
    // Raise the Windows timer resolution to 1ms. CRITICAL for menu/sparse-workload framerate: the game's
    // VI-synced RCP scheduler + our runtime's gfx/task threads hand work off via timed waits. At the default
    // ~15.6ms timer resolution, each wake quantizes to ~15.6ms, so a single gfx round-trip costs ~2 quanta
    // (~32ms) and a menu frame several — capping the menu at ~10fps. (A race never waits — it always has gfx
    // queued — so it was unaffected and this was wrongly dismissed as "jitter only".) winmm is linked.
    if (std::getenv("BAR_NO_TIMER_FIX") == nullptr) timeBeginPeriod(1);  // R6 bisect: BAR_NO_TIMER_FIX=1 reverts this menu-FPS fix
#endif

    recomp::Version project_version{};
    if (!recomp::Version::from_string(version_string, project_version)) {
        message_box(("Invalid version string: " + version_string).c_str());
        return EXIT_FAILURE;
    }

    // Config/save directory: a real per-user folder (%LOCALAPPDATA%\BeetleRecomp on Windows;
    // ~/.config/BeetleRecomp on Linux), or the exe dir when a portable.txt is present. librecomp
    // writes saves / mod config here.
    recomp::register_config_path(bar::config::get_app_config_directory());

    // Load persisted graphics settings (or write defaults on first run) and push them into the
    // runtime. This also enables RT64 high-FPS frame interpolation: the default config sets
    // rr_option = Display, so presentation tracks the monitor refresh while the game/VI/audio
    // clock stays locked at its native 60 Hz (identical game speed — see
    // docs/SETTINGS_MENU_AND_HIGH_FPS.md). Must run before recomp::start() so the renderer reads
    // it when it constructs the RT64 context.
    bar::config::load_and_apply_graphics();

    // Load persisted cheat toggles (applied per-frame from the SI hook once the game runs; also
    // editable live via the in-game cheats menu — F1).
    bar_cheats::load();

    // Teach librecomp about BAR (hash / entrypoint / save type).
    for (const auto& game : supported_games) {
        recomp::register_game(game);
    }

    // Wire the generated section/overlay tables so BAR's ~130 relocatable modules resolve at runtime.
    bar_register_overlays();
    // TODO(BAR): bar::register_patches(); once patches exist.

    // ---- ROM source ----
    // The launcher's "Play" button loads this ROM; with the UI disabled it's the auto-start ROM.
    g_default_rom_path = (argc > 1)
        ? std::filesystem::path(argv[1])
        : std::filesystem::path("C:/Users/Bryan/Downloads/Beetle Adventure Racing (USA).z64"); // TODO(BAR): native ROM picker

    std::u8string game_id = supported_games[0].game_id;

    // Coordinator: the game thread blocks in wait_for_game_started() until start_game() runs. The VI
    // thread only seeds a dummy OSViMode while !is_game_started(); starting before its first tick
    // makes update_vi() deref a null mode — so we wait for g_bar_vi_ticked (renderer presented a
    // frame, which also means the launcher can draw over it). recomp::start() below blocks the main
    // thread, so this runs on a detached thread.
    std::thread([game_id]() mutable {   // mutable: recomp::select_rom takes a non-const std::u8string&
        using namespace std::chrono_literals;
        for (int i = 0; i < 500 && !g_bar_vi_ticked.load(); i++) {
            std::this_thread::sleep_for(10ms);
        }

        std::filesystem::path rom = g_default_rom_path;
        // Start the game right away — do NOT wait for "Play". The launcher/settings/cheats menu is an
        // OVERLAY drawn over the running game: RT64 only drives the UI's render+input render-hook while
        // the game is producing frames, so the menu cannot be interactive before the game starts. The
        // launcher stays shown over the (booting) game until the user clicks Play (which hides it);
        // F1 reopens the menu in-game. BAR_SKIP_LAUNCHER starts with the overlay hidden (headless).

        recomp::RomValidationError rom_err = recomp::select_rom(rom, game_id);
        if (rom_err != recomp::RomValidationError::Good) {
            message_box(("Could not load ROM \"" + rom.string() + "\" (validation error "
                         + std::to_string(static_cast<int>(rom_err)) + ")").c_str());
            return;
        }
        recomp::start_game(game_id);
#ifdef BEETLE_ENABLE_UI
        bar_ui::on_game_started();   // mark game started (enables F1); the launcher stays as an overlay
#endif
    }).detach();

    // ---- Assemble callbacks (field names verified against the vendored headers) ----
    recomp::rsp::callbacks_t rsp_callbacks{};                  // MANDATORY
    rsp_callbacks.get_rsp_microcode = get_rsp_microcode;

    ultramodern::renderer::callbacks_t renderer_callbacks{};   // MANDATORY
    renderer_callbacks.create_render_context = bar_create_rt64_render_context;

    ultramodern::gfx_callbacks_t gfx_callbacks{};
    gfx_callbacks.create_gfx    = create_gfx;
    gfx_callbacks.create_window = create_window;
    gfx_callbacks.update_gfx    = update_gfx;

    ultramodern::audio_callbacks_t audio_callbacks{};
    audio_callbacks.queue_samples        = queue_samples;
    audio_callbacks.get_frames_remaining = get_frames_remaining;
    audio_callbacks.set_frequency        = set_frequency;

    ultramodern::input::callbacks_t input_callbacks{};
    input_callbacks.poll_input = input_poll;
    input_callbacks.get_input  = input_get;
    input_callbacks.set_rumble = input_set_rumble;
    input_callbacks.get_connected_device_info = input_get_device_info;

    ultramodern::error_handling::callbacks_t error_handling_callbacks{};
    error_handling_callbacks.message_box = message_box;

    ultramodern::threads::callbacks_t threads_callbacks{};
    threads_callbacks.get_game_thread_name = get_game_thread_name;

    recomp::Configuration config{};
    config.project_version          = project_version;
    config.rsp_callbacks            = rsp_callbacks;
    config.renderer_callbacks       = renderer_callbacks;
    config.audio_callbacks          = audio_callbacks;
    config.input_callbacks          = input_callbacks;
    config.gfx_callbacks            = gfx_callbacks;
    config.error_handling_callbacks = error_handling_callbacks;
    config.threads_callbacks        = threads_callbacks;
    // MENU-FPS FIX: BAR's SI (controller) event queue (main.c:311, D_8002F828) is only capacity 3, and our
    // low-level __osSiRawStartDma stub posts an SI completion per call. BAR doesn't drain them all during
    // the menu, so the queue fills with stale completions; with the default requeue_si=true, every new SI
    // completion then BOUNCES (re-queues) off the full queue in a ~15M/sec busy-spin that starves the
    // cooperative scheduler and throttled the menu to ~10fps. SI completions are disposable (live input is
    // re-read from PIF RAM each frame), so drop the excess instead of bouncing it.
    config.message_queue_control.requeue_si = (std::getenv("BAR_REQUEUE_SI") != nullptr);  // R6 bisect: default false (the menu-FPS fix); BAR_REQUEUE_SI=1 reverts to stock true
    // config.window_handle / events_callbacks: defaults are fine for now.

    // Cooperative audio-thread preemption: raise a host "yield now" flag at ~500Hz; a poll injected at every
    // recompiled-function prologue (fix-recompiled.sh rule F) then yields to the audio manager so long
    // straight-line game-thread compute can't starve audio (-> no underrun crackle at a low buffer). The flag
    // is gated to game threads once running, so starting the timer before the game spins up is harmless.
    extern void bar_start_preempt_timer();
    extern void bar_stop_preempt_timer();
    bar_start_preempt_timer();

    // Blocks until the game exits.
    recomp::start(config);
    bar_stop_preempt_timer();
    return EXIT_SUCCESS;
}
