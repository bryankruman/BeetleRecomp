static ultramodern::gfx_callbacks_t::gfx_data_t create_gfx() {
    SDL_SetMainReady();
    if (SDL_Init(SDL_INIT_VIDEO | SDL_INIT_EVENTS | SDL_INIT_GAMECONTROLLER | SDL_INIT_AUDIO) != 0) {
        std::fprintf(stderr, "[BeetleRecomp] SDL_Init failed: %s\n", SDL_GetError());
    }
    return nullptr;   // gfx_data_t is unused; the window is tracked in g_window
}

static ultramodern::renderer::WindowHandle create_window(ultramodern::gfx_callbacks_t::gfx_data_t /*data*/) {
    g_window = SDL_CreateWindow("Beetle Adventure Racing: Recompiled",
        SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 960, 720,
        SDL_WINDOW_RESIZABLE | SDL_WINDOW_ALLOW_HIGHDPI);
    if (g_window == nullptr) {
        std::fprintf(stderr, "[BeetleRecomp] SDL_CreateWindow failed: %s\n", SDL_GetError());
        return {};
    }
    SDL_SysWMinfo wm_info;
    SDL_VERSION(&wm_info.version);
    if (SDL_GetWindowWMInfo(g_window, &wm_info) != SDL_TRUE) {
        std::fprintf(stderr, "[BeetleRecomp] SDL_GetWindowWMInfo failed: %s\n", SDL_GetError());
        return {};
    }
    // create_window runs on the same (main) thread that pumps update_gfx below, so the
    // window's owning thread id is this thread.
    g_bar_hwnd = wm_info.info.win.window;
    return ultramodern::renderer::WindowHandle{ wm_info.info.win.window, GetCurrentThreadId() };
}

static void update_gfx(ultramodern::gfx_callbacks_t::gfx_data_t /*data*/) {
    SDL_Event event;
    while (SDL_PollEvent(&event)) {
        if (event.type == SDL_QUIT) {
            ultramodern::quit();
        }
    }
}

// Audio (audio_callbacks_t) — SDL2 queued-audio backend.
// The libultra->ultramodern AI chain (librecomp/src/ai.cpp -> ultramodern/src/audio.cpp) delivers the
// game's PCM here as `count` interleaved-stereo int16 samples pointing straight into RDRAM (no swap done
// upstream). EMPIRICALLY VERIFIED (captured BAR menu music, offline autocorrelation): the raw int16
// values are ALREADY correct host-order (librecomp's RDRAM word-swizzle yields correct s16 on raw read);
// byteswapping them produces white-noise "screeching". The word-swizzle delivers the pair as [R,L], so
// we channel-swap to [L,R] (matches Zelda64Recompiled's backend). BAR_AUDIO_XFORM overrides:
//   0 = as-is, 1 = byteswap only, 2 = channel-swap only [default, correct], 3 = byteswap + channel-swap.
//
// SAFETY: the output device is opened (and sound produced) ONLY when BAR_AUDIO_PLAY is set. By default —
// and while verifying correctness — NO device is opened and NOTHING plays. BAR_AUDIO_CAPTURE=<path> dumps
// the raw (untransformed) received stream so the correct transform can be checked offline without audio.
static SDL_AudioDeviceID g_audio_dev = 0;
static uint32_t g_dev_freq  = 0;        // freq the SDL device is currently open at
static uint32_t g_game_freq = 0;        // last freq the game requested (for capture/WAV)
static constexpr int kBarBytesPerFrame = 2 * (int)sizeof(int16_t);   // stereo s16
static std::FILE* g_audio_capture = nullptr;
static size_t g_audio_capture_bytes = 0;

static bool bar_audio_play_enabled() {
    // Audio output ON by default; BAR_NO_AUDIO disables the device (and the ucode, see get_rsp_microcode).
    static const bool on = std::getenv("BAR_NO_AUDIO") == nullptr;
    return on;
}
static int bar_audio_xform() {
    static const int x = []{ const char* e = std::getenv("BAR_AUDIO_XFORM"); return e ? std::atoi(e) : 2; }();
    return x;
}

static void bar_open_audio(uint32_t freq) {
    if (freq == 0) return;
    g_game_freq = freq;
    if (g_audio_capture == nullptr) {
        static bool tried = false;
        if (!tried) { tried = true;
            const char* cap = std::getenv("BAR_AUDIO_CAPTURE");
            if (cap && *cap) { g_audio_capture = std::fopen(cap, "wb");
                std::fprintf(stderr, "[BeetleRecomp] audio capture -> %s\n", cap); }
        }
    }
    if (!bar_audio_play_enabled()) return;          // muted: never open a device, never play
    if (g_audio_dev != 0 && g_dev_freq == freq) return;
    if (g_audio_dev != 0) { SDL_CloseAudioDevice(g_audio_dev); g_audio_dev = 0; }
    if (SDL_WasInit(SDL_INIT_AUDIO) == 0 && SDL_InitSubSystem(SDL_INIT_AUDIO) != 0) {
        std::fprintf(stderr, "[BeetleRecomp] SDL_InitSubSystem(AUDIO) failed: %s\n", SDL_GetError());
        return;
    }
    SDL_AudioSpec want{}, have{};
    want.freq = (int)freq; want.format = AUDIO_S16SYS; want.channels = 2; want.samples = 1024; want.callback = nullptr;
    g_audio_dev = SDL_OpenAudioDevice(nullptr, 0, &want, &have, 0);
    if (g_audio_dev == 0) {
        std::fprintf(stderr, "[BeetleRecomp] SDL_OpenAudioDevice failed: %s\n", SDL_GetError());
        return;
    }
    g_dev_freq = (uint32_t)have.freq;
    SDL_PauseAudioDevice(g_audio_dev, 0);
}

static void queue_samples(int16_t* samples, size_t count) {
    if (samples == nullptr || count == 0) return;
    // Capture the RAW received stream (no transform) for offline analysis, capped at ~16 MB.
    if (g_audio_capture && g_audio_capture_bytes < (16u << 20)) {
        size_t n = std::fwrite(samples, sizeof(int16_t), count, g_audio_capture);
        g_audio_capture_bytes += n * sizeof(int16_t);
    }
    if (g_audio_dev == 0) return;                    // muted (default) — produce no sound
    const int xf = bar_audio_xform();
    const bool byteswap = (xf == 1 || xf == 3);
    const bool chanswap = (xf == 2 || xf == 3);
    static std::vector<int16_t> out;
    out.resize(count);
    for (size_t i = 0; i < count; i++) {
        uint16_t v = (uint16_t)samples[i];
        out[i] = byteswap ? (int16_t)(uint16_t)((v >> 8) | (v << 8)) : (int16_t)v;
    }
    if (chanswap) { for (size_t i = 0; i + 1 < count; i += 2) { int16_t t = out[i]; out[i] = out[i+1]; out[i+1] = t; } }
    { static const bool dbg = std::getenv("BAR_AUDIO_DBG") != nullptr; if (dbg) {
        extern int g_bar_audio_peak; extern long g_bar_audio_clip;
        for (size_t i = 0; i < count; i++) { int a = out[i] < 0 ? -(int)out[i] : out[i];
            if (a > g_bar_audio_peak) g_bar_audio_peak = a; if (a >= 32760) g_bar_audio_clip++; } } }
    SDL_QueueAudio(g_audio_dev, out.data(), (Uint32)(count * sizeof(int16_t)));
}

int  g_bar_audio_peak = 0;   // diag (BAR_AUDIO_DBG): peak |sample| + saturation count
long g_bar_audio_clip = 0;

static size_t get_frames_remaining() {
    if (g_audio_dev == 0) return 0;
    size_t frames = (size_t)(SDL_GetQueuedAudioSize(g_audio_dev) / (Uint32)kBarBytesPerFrame);
    { static const bool dbg = std::getenv("BAR_AUDIO_DBG") != nullptr; if (dbg) {
        // Inter-call wall delta (called ~once per game frame). Flag genuine near-underruns (queue almost
        // empty) and egregious single-frame stalls (>50 ms = >3 VI) — not normal 16.6 ms frames.
        static auto last = std::chrono::steady_clock::now(); auto now = std::chrono::steady_clock::now();
        double dms = std::chrono::duration<double, std::milli>(now - last).count(); last = now;
        if ((frames < 150 && frames > 0) || dms > 50.0) { std::fprintf(stderr, "[BeetleRecomp] audio LOW: queued=%zu frameDelta=%.1fms\n", frames, dms); std::fflush(stderr); }
        static size_t minq = (size_t)-1, maxq = 0; static int n = 0;
        if (frames < minq) minq = frames; if (frames > maxq) maxq = frames;
        if ((++n % 60) == 0) {
            std::fprintf(stderr, "[BeetleRecomp] audio: queued now=%zu min=%zu max=%zu | peak=%d clipped=%ld%s\n",
                         frames, minq, maxq, g_bar_audio_peak, g_bar_audio_clip,
                         (minq == 0) ? "  <<< UNDERRUN" : "");
            std::fflush(stderr); minq = (size_t)-1; maxq = 0; g_bar_audio_peak = 0; g_bar_audio_clip = 0;
        } } }
    return frames;
}

